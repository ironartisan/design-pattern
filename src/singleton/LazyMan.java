public class LazyMan {
    //定义实例
    private static LazyMan instance;

    //私有构造方法
    private LazyMan(){
    }

    // 定义静态工厂方法
    public static LazyMan getInstance(){
        //在对象被使用的时候才实例化
        if ( instance == null) {
            instance = new LazyMan();
        }
        return instance;
    }
}
